<p><strong>Calculate Shipping Costs</strong></p>
<p>Shipping costs are calculated individually for each order based on the number and weight of items in your order, as well as the shipping method chosen. For this reason, the price displayed on product information pages does not include shipping.</p>
<p>To estimate shipping cost for any product item or combination of items:</p>
<p>l'On the item's product information page, click the Add to Cart button  <img src="http://www.myefox.com/images/s/image0014.gif" /> . You will be then taken to your Shopping Cart page.</p>
<p>l'View the total shipping costs for the items in your cart under Estimate Shipping Costs at the bottom of the page.</p>
<p>l  You can also access your Shopping Cart at any time by clicking the Cart  <img src="http://www.myefox.com/images/s/image0015.jpg" />  at the top of every page on Myefox.com.</p>
<p>Note: Adding an item to your shopping cart puts you under no obligation to order. If after viewing estimated shipping costs you no longer wish to order an item, simply click Delete<img src="http://www.myefox.com/images/s/image0016.png" />   next to the item name in your shopping cart to remove it.</p>
