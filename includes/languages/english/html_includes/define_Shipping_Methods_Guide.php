<p><strong>Estimate Delivery Time</strong></p>
<p>You can choose the shipping method you want on the <strong>Billing, Shipping, and Review</strong> page during the Checkout process. There are three main shipping methods to choose from:</p>
<p><strong>Expedited Shipping</strong></p>
<p>This shipping method is the fastest available. Delivery times are between 3 - 5 days to all major destinations. Shipping costs vary with item, but all orders that use expedited shipping will receive a 36% discount on shipping costs.</p>
<p><strong>Standard Shipping</strong></p>
<p>Standard shipping takes a bit longer than expedited shipping but costs less. Delivery times are between 6 - 8 days to all major destinations. Shipping costs vary with item, but all orders that use standard shipping will receive a 26% discount on shipping costs.</p>
<p>Super Saver Shipping</p>
<p>Delivery times are between 15 - 30 days for all major destinations. Although the delivery time is much longer, shipping costs are much lower when this option is chosen. Order tracking may not always be available with Super Saver Shipping. Please take this into consideration when choosing this option.</p>
<p><strong>The above delivery estimates do not include order processing time. See estimate total delivery time for more details.</strong></p>
<p>Some of our items also have extra savings on shipping. Look out for following stickers on our products:</p>
